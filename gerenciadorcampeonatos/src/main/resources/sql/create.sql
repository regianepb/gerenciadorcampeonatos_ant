CREATE TABLE IF NOT EXISTS DESPESA(
   ID BIGINT AUTO_INCREMENT NOT NULL,
   DESCRICAO VARCHAR(100) NOT NULL
   PRIMARY KEY (ID)
)ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS RECEITA(
   ID BIGINT AUTO_INCREMENT NOT NULL,
   DESCRICAO VARCHAR(100) NOT NULL
   PRIMARY KEY (ID)
)ENGINE=InnoDB;


CREATE TABLE IF NOT EXISTS CAMPEONATO_JOGADOR (
   CAMPEONATO_ID BIGINT NULL,
   JOGADOR_ID BIGINT NULL,
   VALOR DECIMAL NOT NULL,
   PRIMARY KEY (CAMPEONATO_ID, JOGADOR_ID), 
   CONSTRAINT FK_CAMPEONATO_JOGADOR_CAMPEONATO FOREIGN KEY (CAMPEONATO_ID) REFERENCES CAMPEONATO(ID), 
   CONSTRAINT FK_CAMPEONATO_JOGADOR_JOGADOR FOREIGN KEY (JOGADOR_ID) REFERENCES JOGADOR(ID) 
